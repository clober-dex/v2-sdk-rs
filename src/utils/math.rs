use std::str::FromStr;

use alloy::primitives::{I256, U256};

// make I256 from unsigned literal (no From<u128> in alloy for I256)
#[inline]
pub fn i256_u(x: u128) -> I256 {
    I256::from_raw(U256::from(x))
}

// safe cast I256 â†’ i128 (our ticks fit well within 128 bits)
#[inline]
pub fn i256_to_i128(x: I256) -> i128 {
    let neg = x.is_negative();
    let mut y = x;
    if neg {
        y = -y;
    }
    let mag_u: U256 = y.into_raw();
    let lo: u128 = mag_u.to::<u128>();
    let val = lo as i128;
    if neg { -val } else { val }
}

// decimal-to-I256 (non-negative only)
#[inline]
pub fn i256_dec(s: &str) -> I256 {
    let u = U256::from_str(s).expect("parse const");
    I256::from_raw(u)
}

// ceil-div helper for U256 (ts divide with roundUp)
#[inline]
pub fn divide_u256(x: U256, y: U256, round_up: bool) -> U256 {
    if !round_up {
        return x / y;
    }
    if x.is_zero() { U256::ZERO } else { ((x - U256::from(1u8)) / y) + U256::from(1u8) }
}

/* ===== exact 1:1 port of lnWad (bit ops + LUT path preserved) ===== */
pub fn ln_wad(mut x: U256) -> I256 {
    assert!(x > U256::ZERO, "ln_wad: x must be > 0");

    // lambdas from ts
    #[inline]
    fn or_(a: U256, b: U256) -> U256 {
        a | b
    }
    #[inline]
    fn xor_(a: U256, b: U256) -> U256 {
        a ^ b
    }
    #[inline]
    fn and_(a: U256, b: U256) -> U256 {
        a & b
    }
    #[inline]
    fn add_(a: I256, b: I256) -> I256 {
        a + b
    }
    #[inline]
    fn sub_(a: I256, b: I256) -> I256 {
        a - b
    }
    #[inline]
    fn mul_(a: I256, b: I256) -> I256 {
        a * b
    }
    #[inline]
    fn sdiv_(a: I256, b: I256) -> I256 {
        a / b
    }
    #[inline]
    fn lt_(a: U256, b: U256) -> U256 {
        if a < b { U256::from(1u8) } else { U256::ZERO }
    }
    #[inline]
    fn shl_(x: U256, y: U256) -> U256 {
        y << x.to::<u32>()
    }
    #[inline]
    fn shr_(x: U256, y: U256) -> U256 {
        y >> x.to::<u32>()
    }
    #[inline]
    fn sar_(x: U256, y: I256) -> I256 {
        y >> x.to::<u32>()
    }
    #[inline]
    fn byte_(i: U256, x: U256) -> U256 {
        let idx = i.to::<u32>();
        let shift = 248i32 - (idx as i32) * 8;
        let s = if shift <= 0 { 0 } else { shift as u32 };
        (x >> s) & U256::from(0xffu16)
    }

    // masks (same semantics as ts)
    let mask128 = (U256::from(1u8) << 128) - U256::from(1u8);
    let mask64 = (U256::from(1u8) << 64) - U256::from(1u8);
    let mask32 = (U256::from(1u8) << 32) - U256::from(1u8);
    let mask16 = (U256::from(1u8) << 16) - U256::from(1u8);
    let mask8 = (U256::from(1u8) << 8) - U256::from(1u8);

    // magic constants (same as ts hex), as runtime locals (const fn not required)
    let table_selector = U256::from_limbs([
        0xcc63_18c6_db6d_54be, // lo
        0x8421_0842_1084_2108, // hi
        0,
        0,
    ]);
    let table_lut = U256::from_limbs([
        0xfafa_fcfb_ffff_ffff, // lo
        0xf9fa_fdfa_fcfc_fbfe,
        0xf9fd_fcfd_fafb_fcfe,
        0xf8f9_f9fa_f9fd_fafb, // hi
    ]);

    // r build (identical sequence)
    let mut r = shl_(U256::from(7u8), lt_(mask128, x));
    r = or_(r, shl_(U256::from(6u8), lt_(mask64, shr_(r, x))));
    r = or_(r, shl_(U256::from(5u8), lt_(mask32, shr_(r, x))));
    r = or_(r, shl_(U256::from(4u8), lt_(mask16, shr_(r, x))));
    r = or_(r, shl_(U256::from(3u8), lt_(mask8, shr_(r, x))));
    r = xor_(r, byte_(and_(U256::from(0x1fu8), shr_(shr_(r, x), table_selector)), table_lut));

    // range reduction: x = shr(159, shl(r, x))
    x = shr_(U256::from(159u32), shl_(r, x));

    // p polynomial in 2**192 basis (exact nesting)
    let xi = I256::from_raw(x);
    let mut p = sub_(
        sar_(
            U256::from(96u32),
            mul_(
                add_(
                    i256_dec("43456485725739037958740375743393"),
                    sar_(
                        U256::from(96u32),
                        mul_(
                            add_(
                                i256_dec("24828157081833163892658089445524"),
                                sar_(
                                    U256::from(96u32),
                                    mul_(add_(i256_dec("3273285459638523848632254066296"), xi), xi),
                                ),
                            ),
                            xi,
                        ),
                    ),
                ),
                xi,
            ),
        ),
        i256_dec("11111509109440967052023855526967"),
    );
    p = sub_(sar_(U256::from(96u32), mul_(p, xi)), i256_dec("45023709667254063763336534515857"));
    p = sub_(sar_(U256::from(96u32), mul_(p, xi)), i256_dec("14706773417378608786704636184526"));
    p = sub_(mul_(p, xi), (i256_u(795_164_235_651_350_426_258_249_787_498u128) << 96));

    // q polynomial (monic)
    let mut q = add_(i256_dec("5573035233440673466300451813936"), xi);
    q = add_(i256_dec("71694874799317883764090561454958"), sar_(U256::from(96u32), mul_(xi, q)));
    q = add_(i256_dec("283447036172924575727196451306956"), sar_(U256::from(96u32), mul_(xi, q)));
    q = add_(i256_dec("401686690394027663651624208769553"), sar_(U256::from(96u32), mul_(xi, q)));
    q = add_(i256_dec("204048457590392012362485061816622"), sar_(U256::from(96u32), mul_(xi, q)));
    q = add_(i256_dec("31853899698501571402653359427138"), sar_(U256::from(96u32), mul_(xi, q)));
    q = add_(i256_dec("909429971244387300277376558375"), sar_(U256::from(96u32), mul_(xi, q)));

    // p/q
    p = sdiv_(p, q);

    // scale: s * 5**18 * 2**96
    p = mul_(i256_dec("1677202110996718588342820967067443963516166"), p);

    // add ln(2) * (159 - r) * 5**18 * 2**192
    let r_u: u32 = r.to::<u32>();
    let k_i256 = I256::from_raw(U256::from(159u8)) - I256::from_raw(U256::from(r_u as u64));
    p = add_(
        mul_(
            i256_dec("16597577552685614221487285958193947469193820559219878177908093499208371"),
            k_i256,
        ),
        p,
    );

    // base conversion: 2**96 / (5**18 * 2**192)
    sdiv_(p, i256_dec("302231454903657293676544000000000000000000"))
}

